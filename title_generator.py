# -*- coding: utf-8 -*-
"""
Генератор названий схем на основе метаданных проекта.

Используемые метаданные:
- 1_1_object_full_name: Полное наименование объекта
"""

from typing import Dict, Any


class TitleGenerator:
    """Генератор названий на основе метаданных проекта"""

    def __init__(self):
        """Инициализация генератора"""
        pass

    def generate_scheme_title(self, metadata: Dict[str, Any]) -> str:
        """
        Сгенерировать название схемы для компоновки.

        Префикс "Схема границ территории. " добавляется автоматически.
        Обрамляющие кавычки вокруг full_name УДАЛЕНЫ — кавычки часто
        проставляются по-разному (французские «», программные "",
        типографские ""), пользователь сам указывает финальное название
        с нужным оформлением через метаданные проекта.

        Args:
            metadata: Словарь метаданных проекта. Используется только
                ключ `1_1_object_full_name`.

        Returns:
            Заголовок вида `Схема границ территории. {full_name}`.
        """
        full_name = metadata.get("1_1_object_full_name", "")
        return f"Схема границ территории. {full_name}"
